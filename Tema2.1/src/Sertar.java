
public class Sertar {

	int latime;
	int lungime;
	int inaltime;
	
	@Override
	public String toString() {
		return "Sertar [" + latime + "-" + lungime + "-" + inaltime + "]";
	}



	public Sertar(int latime, int lungime, int inaltime) {
		
		this.latime = latime;
		this.lungime = lungime;
		this.inaltime = inaltime;
		
	}

	
}
